// goldenSchema returns a fixed schema used for golden output tests.
func goldenSchema() *ir.Schema {
	return &ir.Schema{
		Datasource: &ir.Datasource{Provider: "postgresql"},
		Generators: []*ir.Generator{{Name: "client", Provider: "gco-go", Output: "./gen", Package: "db"}},
		Models: []*ir.Model{
			{
				Name: "User",
				Fields: []*ir.Field{
					{Name: "id", Type: ir.FieldKindScalar, ScalarType: "String", IsID: true},
					{Name: "email", Type: ir.FieldKindScalar, ScalarType: "String", IsUnique: true},
					{Name: "name", Type: ir.FieldKindScalar, ScalarType: "String", IsOptional: true},
					{Name: "createdAt", Type: ir.FieldKindScalar, ScalarType: "DateTime"},
				},
				PrimaryKey: &ir.PrimaryKey{Fields: []string{"id"}},
			},
			{
				Name: "Post",
				Fields: []*ir.Field{
					{Name: "id", Type: ir.FieldKindScalar, ScalarType: "String", IsID: true},
					{Name: "title", Type: ir.FieldKindScalar, ScalarType: "String"},
					{Name: "published", Type: ir.FieldKindScalar, ScalarType: "Boolean"},
					{Name: "authorId", Type: ir.FieldKindScalar, ScalarType: "String"},
					{Name: "author", Type: ir.FieldKindRelation, ModelType: "User"},
				},
				PrimaryKey: &ir.PrimaryKey{Fields: []string{"id"}},
			},
		},
		Enums: []*ir.Enum{
			{Name: "Role", Values: []*ir.EnumValue{{Name: "USER"}, {Name: "ADMIN"}}},
		},
	}
}

// generateGoldenFiles is a helper that generates all files from the golden schema.
func generateGoldenFiles(t *testing.T) []*GeneratedFile {
	t.Helper()
	g := NewGenerator(goldenSchema())
	files, err := g.Generate()
	if err != nil {
		t.Fatalf("Generate() error: %v", err)
	}
	return files
}

// fileContent returns the string content of the generated file at the given path.
func fileContent(t *testing.T, files []*GeneratedFile, path string) string {
	t.Helper()
	for _, f := range files {
		if f.Path == path {
			return string(f.Content)
		}
	}
	t.Fatalf("file %q not found in generated output", path)
	return ""
}

