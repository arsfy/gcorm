func TestGoldenQueryOutput(t *testing.T) {
	files := generateGoldenFiles(t)

	// Check User query file.
	userQuery := fileContent(t, files, "query/user.go")
	userChecks := []string{
		"Equals(",
		"Not(",
		"In(",
		"Contains(", // String fields should have Contains
		"Set(",      // create/update set
		"Asc()",
		"Desc()",
	}
	for _, check := range userChecks {
		if !strings.Contains(userQuery, check) {
			t.Errorf("query/user.go missing %q", check)
		}
	}
	// DateTime field (createdAt) should have Lt/Gt.
	if !strings.Contains(userQuery, "Lt(") {
		t.Error("query/user.go missing Lt( for DateTime field")
	}
	if !strings.Contains(userQuery, "Gt(") {
		t.Error("query/user.go missing Gt( for DateTime field")
	}

	// New features: QueryOption, AND/OR/NOT, Cursor, Aggregate/GroupBy, CreateInput.
	newQueryChecks := []string{
		"UserQueryOption",
		"UserQueryConfig",
		"var User = UserQuery{",
		"ApplyUserOptions",
		"UserTakeOption",
		"UserSkipOption",
		"func (UserQuery) AND(",
		"func (UserQuery) OR(",
		"func (UserQuery) NOT(",
		"func (UserQuery) Cursor(",
		"UserAggregateOption",
		"UserAggregateResult",
		"UserGroupByResult",
		"UserCreateInput",
		"UserCreateNestedInput",
		"UserWhereUniqueInput",
		"ScalarValues()",
		`const UserTable = "User"`,
		`const UserCreatedAtColumn = "created_at"`,
	}
	for _, check := range newQueryChecks {
		if !strings.Contains(userQuery, check) {
			t.Errorf("query/user.go missing %q", check)
		}
	}
	if !regexp.MustCompile(`(?m)\bEmail\s+userEmailField\b`).MatchString(userQuery) {
		t.Error("query/user.go should scope Email under UserQuery")
	}
	if !regexp.MustCompile(`(?m)\bCreatedAt\s+userCreatedAtField\b`).MatchString(userQuery) {
		t.Error("query/user.go should scope CreatedAt under UserQuery")
	}
	if !strings.Contains(userQuery, "func (userNameField) Set(v string) UserSetClause") {
		t.Error("query/user.go should allow plain values in Set for optional string fields")
	}
	if !strings.Contains(userQuery, "func (userNameField) SetNull() UserSetClause") {
		t.Error("query/user.go should generate SetNull for optional string fields")
	}
	if strings.Contains(userQuery, "var UserEmail =") {
		t.Error("query/user.go should not generate flattened UserEmail helper vars")
	}

	// In method should convert to []any for type-safe SQL building.
	if !strings.Contains(userQuery, "iVals := make([]any, len(vals))") {
		t.Error("query/user.go In() should convert to []any")
	}

	// Check Post query file.
	postQuery := fileContent(t, files, "query/post.go")
	postChecks := []string{
		"Equals(",
		"Not(",
		"In(",
		"Set(",
		"Asc()",
		"Desc()",
		"var Post = PostQuery{",
		"PostQueryOption",
		"PostCreateInput",
	}
	for _, check := range postChecks {
		if !strings.Contains(postQuery, check) {
			t.Errorf("query/post.go missing %q", check)
		}
	}
	if !regexp.MustCompile(`(?m)\bAuthor\s+postAuthorRelation\b`).MatchString(postQuery) {
		t.Error("query/post.go should scope Author under PostQuery")
	}
}

func TestGoldenColumnNamesUseResolvedDBName(t *testing.T) {
	schema := &ir.Schema{
		Datasource: &ir.Datasource{Provider: "postgresql"},
		Generators: []*ir.Generator{{Name: "go", Package: "db", Output: "./gen"}},
		Models: []*ir.Model{
			{
				Name: "Thing",
				Fields: []*ir.Field{
					{Name: "id", Type: ir.FieldKindScalar, ScalarType: "String", IsID: true},
					{Name: "abcAbc", Type: ir.FieldKindScalar, ScalarType: "String"},
					{Name: "mappedField", DBName: "custom_column", Type: ir.FieldKindScalar, ScalarType: "String"},
				},
				PrimaryKey: &ir.PrimaryKey{Fields: []string{"id"}},
			},
		},
	}
	g := NewGenerator(schema)
	files, err := g.Generate()
	if err != nil {
		t.Fatalf("Generate() error: %v", err)
	}

	models := fileContent(t, files, "model/models.go")
	if !strings.Contains(models, `db:"abc_abc"`) {
		t.Fatalf("model should use snake_case db tag for abcAbc:\n%s", models)
	}
	if !strings.Contains(models, `db:"custom_column"`) {
		t.Fatalf("model should respect explicit DBName:\n%s", models)
	}

	query := fileContent(t, files, "query/thing.go")
	if !strings.Contains(query, `Field: "abc_abc"`) {
		t.Fatalf("query should use snake_case column for abcAbc:\n%s", query)
	}
	if !strings.Contains(query, `Field: "custom_column"`) {
		t.Fatalf("query should respect explicit DBName:\n%s", query)
	}
	if strings.Contains(query, `Field: "abcAbc"`) {
		t.Fatalf("query should not use raw camelCase field name:\n%s", query)
	}
}

