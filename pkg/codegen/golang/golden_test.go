func TestGoldenDeterminism(t *testing.T) {
	schema := goldenSchema()

	g1 := NewGenerator(schema)
	files1, err := g1.Generate()
	if err != nil {
		t.Fatalf("first Generate() error: %v", err)
	}

	g2 := NewGenerator(schema)
	files2, err := g2.Generate()
	if err != nil {
		t.Fatalf("second Generate() error: %v", err)
	}

	if len(files1) != len(files2) {
		t.Fatalf("file counts differ: %d vs %d", len(files1), len(files2))
	}

	for i := range files1 {
		if files1[i].Path != files2[i].Path {
			t.Errorf("path[%d]: %q vs %q", i, files1[i].Path, files2[i].Path)
		}
		if !bytes.Equal(files1[i].Content, files2[i].Content) {
			t.Errorf("content differs for %s", files1[i].Path)
		}
	}
}

func TestGoldenDoNotEditHeader(t *testing.T) {
	files := generateGoldenFiles(t)
	header := "// Code generated by GCORM. DO NOT EDIT."

	for _, f := range files {
		content := string(f.Content)
		if !strings.HasPrefix(content, header) {
			t.Errorf("file %s does not start with DO NOT EDIT header;\nstarts with: %q",
				f.Path, content[:min(len(content), 60)])
		}
	}
}
